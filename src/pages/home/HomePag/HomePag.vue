        只有互相麻烦的感情，才更深厚。

        我饿了你帮我倒杯水，你饿了我帮你煮碗面，两个人的感情就在这样的相互“麻烦”中慢慢变得坚固。

        彼此依赖，彼此需要的关系，才是良性且稳定的关系</p>
      <p>以前看到过这样一个小故事：

        一小孩搬石头，爸爸鼓励说：只要全力以赴，一定搬得起来！

        可是孩子使足了劲，最后也没搬起石头。他告诉爸爸：我已经竭尽全力了！

        父亲答：你没有竭尽全力，因为我就在你旁边，你都没请我帮助。

        我们从小被教育着“自己的事情自己做”，所以总认为凡事都应该亲力亲为。但却常常忽略了“自己的事情”也分为“自己能做的事情”和“自己做不到的事情”。

        人的能力是有限的，总有些事情是靠自己一个人的力量办不到的。在这种情况下，我们就应该去寻求他人的帮助，而不是自己死磕到底，做无用功。。</p>
