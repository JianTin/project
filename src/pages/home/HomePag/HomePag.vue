        菲菲问她，为什么不找我陪你，为什么不来住我家？你把没把我当成朋友？

        她只是说，我不想麻烦你。

        多客气，也多令人心寒。</p>
      <p>所以整个大学下来，她没有交到一个知心朋友。所以室友结婚，通知了其他所有人，唯独没有通知她。

        有时候，凡事都靠自己，并不是一个好习惯。

